     public void carregar(){
      
            DefaultTableModel tab = (DefaultTableModel) tabela.getModel();
            try {
                this.con = database.getConnection();

                try (PreparedStatement posted = con.prepareStatement("select * from ctenfe where nfeope = 'NFE'"); 
                        ResultSet rs = posted.executeQuery()) {
                    
                    
                    if (rs != null) {
                        while (rs.next()) {
                            tab.addRow(new String[]{
                                rs.getString("NFENUM"),rs.getString("NFECODCLI"),rs.getString("NFENOMECLI"),rs.getString("NFEVLRTOT")});
                        }
                    }
                    
                    con.close();
                }

            } catch (Exception ex) {
                Logger.getLogger(formCadastra.class.getName()).log(Level.SEVERE, null, ex);
            }
       
    }
