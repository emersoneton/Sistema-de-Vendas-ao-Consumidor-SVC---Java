    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked
       try{
            
           int row = tabela.getSelectedRow();
           
           String Table_click=(tabela.getModel().getValueAt(row, 0).toString());

           envia = new formLancaNota();
           
           envia.setVisible(true);
           
           envia.importaNota(Table_click);

           txtNome.setText(Table_click);

           dispose();

        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_tabelaMouseClicked

    private void txtNomeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNomeKeyReleased
           //O DefaultTableModel serve para colocar os dados
        //(colunas e linhas) de uma tabela, separado do objeto tabela (JTable).
        TableRowSorter sorter = null;
        DefaultTableModel modelo = (DefaultTableModel) tabela.getModel();
        sorter = new TableRowSorter<TableModel>(modelo);
        tabela.setRowSorter(sorter);

        String texto = txtNome.getText();
        if(texto.length() == 0){
            sorter.setRowFilter(null);
        }else{
               sorter.setRowFilter(RowFilter.regexFilter(texto));
        }

    }//GEN-LAST:event_txtNomeKeyReleased

