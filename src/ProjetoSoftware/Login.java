    public void UPDATE() throws Exception{
        this.con = database.getConnection();
        String sgl = "UPDATE USER SET USER_STATUS='1' WHERE USER_NOME='"+txtLogin.getText()+"'";
        PreparedStatement editar = con.prepareStatement(sgl);
        //editar.setString(4, "1");
        
        int rowsUpdate = editar.executeUpdate();
        if(rowsUpdate > 0){
            System.out.println("Arquivo atualizado com sucesso!");
        }
        editar.close();
        con.close();
    }
        
