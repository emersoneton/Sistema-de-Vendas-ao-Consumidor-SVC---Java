package ProjetoSoftware;

/**
 *
 * @author Rodrigo
 */
public class Fornecedor {
     int cnpj, cep, phoneRes, phoneCel, codigo, numero;
     String nome, endereco, email, cidade, estado;

    public int getCnpj() {
        return cnpj;
    }

    public void setCnpj(int cnpj) {
        this.cnpj = cnpj;
    }

    public int getCep() {
        return cep;
    }

    public void setCep(int cep) {
        this.cep = cep;
    }

    public int getPhoneRes() {
        return phoneRes;
    }

    public void setPhoneRes(int phoneRes) {
        this.phoneRes = phoneRes;
    }

    public int getPhoneCel() {
        return phoneCel;
    }

    public void setPhoneCel(int phoneCel) {
        this.phoneCel = phoneCel;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }
}
